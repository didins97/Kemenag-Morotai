    AOS.init({
        duration: 800,
        easing: 'ease-in-out',
        once: true
    });

    // Banner slider animation
    let currentSlide = 0;
    const slides = document.querySelectorAll('.banner-slide');
    const totalSlides = slides.length;

    function nextSlide() {
        currentSlide = (currentSlide + 1) % totalSlides;
        document.querySelector('.banner-slider').style.transform = `translateX(-${currentSlide * 100}%)`;
    }

    // Auto slide every 5 seconds
    setInterval(nextSlide, 5000);
</script>

<!-- JavaScript untuk Mobile Navigation -->
<script>
    document.addEventListener('DOMContentLoaded', function() {
        const mobileMenuButton = document.getElementById('mobileMenuButton');
        const mobileCloseButton = document.getElementById('mobileCloseButton');
        const mobileNavContainer = document.getElementById('mobileNavContainer');
        const mobileNavOverlay = document.getElementById('mobileNavOverlay');

        // Toggle mobile menu
        mobileMenuButton.addEventListener('click', function() {
            mobileNavContainer.classList.remove('-translate-x-full');
            mobileNavOverlay.classList.remove('hidden');
            document.body.style.overflow = 'hidden';
        });

        // Close mobile menu
        function closeMobileMenu() {
            mobileNavContainer.classList.add('-translate-x-full');
            mobileNavOverlay.classList.add('hidden');
            document.body.style.overflow = '';
        }

        mobileCloseButton.addEventListener('click', closeMobileMenu);
        mobileNavOverlay.addEventListener('click', closeMobileMenu);

        // Accordion functionality
        document.querySelectorAll('.mobile-accordion-button').forEach(button => {
            button.addEventListener('click', function() {
                const accordion = this.closest('.mobile-accordion');
                const content = this.nextElementSibling;
                const arrow = this.querySelector('.accordion-arrow');

                // Close all other accordions
                document.querySelectorAll('.mobile-accordion-content').forEach(otherContent => {
                    if (otherContent !== content) {
                        otherContent.style.maxHeight = null;
                        otherContent.previousElementSibling.querySelector('.accordion-arrow').classList.remove('rotate-180');
                    }
                });

                // Toggle current accordion
                if (content.style.maxHeight) {
                    content.style.maxHeight = null;
                    arrow.classList.remove('rotate-180');
                } else {
                    content.style.maxHeight = content.scrollHeight + 'px';
                    arrow.classList.add('rotate-180');
                }
            });
        });

        // Sub-accordion functionality
        document.querySelectorAll('.mobile-sub-accordion-button').forEach(button => {
            button.addEventListener('click', function(e) {
                e.stopPropagation(); // Prevent parent accordion from closing

                const subAccordion = this.closest('.mobile-sub-accordion');
                const content = this.nextElementSibling;
                const arrow = this.querySelector('.sub-accordion-arrow');

                // Toggle current sub-accordion
                if (content.style.maxHeight) {
                    content.style.maxHeight = null;
                    arrow.classList.remove('rotate-180');
                } else {
                    content.style.maxHeight = content.scrollHeight + 'px';
                    arrow.classList.add('rotate-180');
                }
            });
        });
    });
</script>
