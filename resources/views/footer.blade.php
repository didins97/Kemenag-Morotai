    // Banner slider animation
    let currentSlide = 0;
    const slides = document.querySelectorAll('.banner-slide');
    const totalSlides = slides.length;

    function nextSlide() {
        currentSlide = (currentSlide + 1) % totalSlides;
        document.querySelector('.banner-slider').style.transform = `translateX(-${currentSlide * 100}%)`;
    }

    // Auto slide every 5 seconds
    setInterval(nextSlide, 5000);
</script>

<!-- JavaScript untuk Mobile Navigation -->
<script>
    const mobileMenu = document.getElementById('mobile-menu');
    const mobileOverlay = document.getElementById('mobile-overlay');
    const mobileMenuButton = document.getElementById('mobileMenuButton');
    const iconMenu = document.getElementById('icon-menu');
    const iconClose = document.getElementById('icon-close');

    // Fungsi Utama Toggle Menu
    function toggleMobileMenu() {
        const isOpen = mobileMenu.classList.contains('translate-x-0');

        // Toggle kelas untuk transisi slide (transform -translate-x-full <-> translate-x-0)
        mobileMenu.classList.toggle('-translate-x-full');
        mobileMenu.classList.toggle('translate-x-0');

        // Toggle Overlay
        mobileOverlay.classList.toggle('hidden');
        setTimeout(() => {
            mobileOverlay.classList.toggle('opacity-0');
        }, isOpen ? 0 : 50); // Tambahkan delay saat membuka agar transisi lebih smooth

        // Toggle Ikon Hamburger ke X
        iconMenu.classList.toggle('hidden');
        iconClose.classList.toggle('hidden');

        // Mengatur overflow body
        document.body.style.overflow = isOpen ? '' : 'hidden';

        // Menutup semua submenu saat menu utama ditutup
        if (isOpen) {
            document.querySelectorAll('div[id$="-mobile"]').forEach(submenu => {
                if (!submenu.classList.contains('hidden')) {
                    submenu.classList.add('hidden');
                    const arrow = document.getElementById(submenu.id + '-arrow');
                    if (arrow) {
                        arrow.classList.remove('rotate-180', 'rotate-90');
                    }
                }
            });
        }
    }

    // Event Listener
    mobileMenuButton.addEventListener('click', toggleMobileMenu);
    mobileOverlay.addEventListener('click', toggleMobileMenu); // Tutup saat klik overlay

    // Logika untuk Toggle Submenu Mobile (Menangani Rotasi Panah)
    function toggleMobileSubmenu(id) {
        const submenu = document.getElementById(id);
        const arrow = document.getElementById(id + '-arrow');

        submenu.classList.toggle('hidden');

        // Rotasi panah
        if (id === 'unitkerja-mobile') {
            arrow.classList.toggle('rotate-90');
        } else {
            arrow.classList.toggle('rotate-180');
        }
    }
</script>
@stack('scripts')
